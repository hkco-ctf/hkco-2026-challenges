void encData(const unsigned char *data, const unsigned int datalen, const unsigned char *key, const unsigned int keylen, unsigned char *out) {
    for (int i = 0; i < datalen; i++) {
        out[i] = (unsigned char)((data[i] + key[i % keylen]) & 0xff);
    }
}

